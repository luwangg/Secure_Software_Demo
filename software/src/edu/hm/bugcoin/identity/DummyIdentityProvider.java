import java.util.ArrayList;
import java.util.List;


/**
 *
 */
public class DummyIdentityProvider implements IdentityProvider
{

    // ----------------------------------------------------------------------------------
    //  Konstanten
    // ----------------------------------------------------------------------------------


    // ----------------------------------------------------------------------------------
    //  Objektvariablen
    // ----------------------------------------------------------------------------------

    private final List<Identity> identities = new ArrayList<>();


    // ----------------------------------------------------------------------------------
    //  Konstruktoren
    // ----------------------------------------------------------------------------------

    public DummyIdentityProvider()
    {
        identities.add(new Identity("marvin", "123", "kladf0u9aufj"));
        identities.add(new Identity("martin", "123", "kladf0u9aufj"));
        identities.add(new Identity("maxi", "123", "26MOCFUXZ5G6MJ2K"));
    }


    // ----------------------------------------------------------------------------------
    //  Getter
    // ----------------------------------------------------------------------------------


    // ----------------------------------------------------------------------------------
    //  Setter
    // ----------------------------------------------------------------------------------


    // ----------------------------------------------------------------------------------
    //  Auskunftsmethoden
    // ----------------------------------------------------------------------------------

    @Override public Identity resolve(String username)
    {
        return identities
                .stream()
                .filter(i -> i.getUsername().equals(username))
                .findFirst().get();
    }

    @Override public void create(final Identity identity)
    {
        identities.add(identity);
    }

    // ----------------------------------------------------------------------------------
    //  Aendernde Methoden
    // ----------------------------------------------------------------------------------


    // ----------------------------------------------------------------------------------
    //  Standardmethoden
    // ----------------------------------------------------------------------------------

}
